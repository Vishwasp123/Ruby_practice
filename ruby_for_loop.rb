puts "Ruby for Loop"
puts "Ruby for loop iterates over a specific range of numbers. Hence, for loop is used if a program has fixed number of iterations."
# syntax:-
# for variable [, variable ...] in expression [do]  
#    code  
# end  

# example:-
a = gets.chomp.to_i   
for i in 1..a do   
  puts i   
end   

