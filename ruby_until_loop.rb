puts "Ruby Until loop"
puts "The Ruby until loop runs until the given condition evaluates to true. It exits the loop when condition becomes true. It is just opposite of the while loop which runs until the given condition evaluates to false."
# Syntax:-
# until conditional  
#    code  
# end 

puts "Example:-"
i = 1   
until i == 10   
    print i*10, "\n"   
    i += 1   
end  